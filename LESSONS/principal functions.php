Variables y tipos de datos

En PHP, las variables se definen con el signo de dólar $ seguido de un nombre de variable. Los tipos de datos que se pueden utilizar en PHP son:

Enteros: int, integer
Flotantes: float, double
Cadenas de texto: string
Boleanos: bool, boolean
Arreglos: array
Objetos: object
Nulos: null
php
Copy code
// Declarar una variable
$nombre = "Juan";
$edad = 30;
$altura = 1.80;
$es_verdadero = true;
$colores = array("rojo", "azul", "verde");
Funciones matemáticas

PHP tiene varias funciones integradas para realizar operaciones matemáticas, como sumas, restas, multiplicaciones y divisiones.

php
Copy code
// Suma
$num1 = 10;
$num2 = 20;
$resultado = $num1 + $num2;
echo "El resultado de la suma es: " . $resultado;

// Resta
$num3 = 50;
$num4 = 30;
$resultado = $num3 - $num4;
echo "El resultado de la resta es: " . $resultado;

// Multiplicación
$num5 = 5;
$num6 = 6;
$resultado = $num5 * $num6;
echo "El resultado de la multiplicación es: " . $resultado;

// División
$num7 = 20;
$num8 = 4;
$resultado = $num7 / $num8;
echo "El resultado de la división es: " . $resultado;
Funciones de cadenas de texto

PHP también tiene funciones para manipular cadenas de texto, como concatenar, obtener la longitud, encontrar una subcadena, etc.

php
Copy code
// Concatenar cadenas de texto
$nombre = "Juan";
$apellido = "Pérez";
$nombre_completo = $nombre . " " . $apellido;
echo "El nombre completo es: " . $nombre_completo;

// Obtener la longitud de una cadena
$cadena = "Hola mundo";
$longitud = strlen($cadena);
echo "La longitud de la cadena es: " . $longitud;

// Encontrar una subcadena
$cadena = "La casa es roja";
$posicion = strpos($cadena, "roja");
echo "La palabra 'roja' empieza en la posición: " . $posicion;
Funciones de arreglos

PHP tiene muchas funciones para trabajar con arreglos, como agregar y eliminar elementos, buscar elementos, ordenar, etc.

php
Copy code
// Crear un arreglo
$colores = array("rojo", "azul", "verde");

// Agregar un elemento al final del arreglo
$colores[] = "amarillo";

// Eliminar un elemento del arreglo
unset($colores[1]);

// Buscar un elemento en el arreglo
$posicion = array_search("verde", $colores);
echo "El elemento 'verde' está en la posición: " . $posicion;

// Ordenar el arreglo en orden ascendente
sort($colores);

// Imprimir todos los elementos del arreglo
foreach ($colores as $color) {
    echo $color . "<br>";
}
